/// Get the CRC 32|64 checksum of the given array.
///
/// Example:
/// ```dart
/// final text = 'hello world';
/// final crc64s = getCrc64(text.codeUnits);
/// print(crc64s);  // 5981764153023615706
/// final crc32s = getCrc32(text.codeUnits);
/// print(crc32s);  // 222957957
///
/// final file = File('README.md');
/// final crc64f = getCrc64(file.readAsBytesSync());
/// print(crc64f);
/// final crc32f = getCrc32(file.readAsBytesSync());
/// print(crc32f)
/// ```

/// Get the CRC-64 checksum of the given array. You can append bytes to an
/// already computed crc by specifying the previous [crc] value.
int getCrc64(List<int> array, [int crc = 0]) => getCrc64_(array, crc);

bool isCrc64Supported() => isCrc64Supported_();

// Works around Dart treating 64 bit integers as signed when shifting.
int shiftDown64(int value) {
  if (value & 0x8000000000000000 == 0) {
    return value >> 8;
  }
  return (value & 0x7fffffffffffffff) >> 8 | 0x0080000000000000;
}

bool isCrc64Supported_() => true;

/// Get the CRC-64 checksum of the given array.
///
/// you can append bytes to an already computed crc by specifying the previous [crc] value.
int getCrc64_(List<int> array, [int crc = 0]) {
  crc ^= 0xffffffffffffffff;
  for (var e in array) {
    crc = _crc64Table[(crc & 0xff) ^ e] ^ shiftDown64(crc);
  }
  return crc ^ 0xffffffffffffffff;
}

